# cafe-analysis.py
# Скрипт для анализа продаж кофе в кафе "Утро"
# Требуется Python 3 с установленным pandas

import pandas as pd  # pandas — библиотека для работы с таблицами

# Шаг 1: загрузка данных из CSV файла
# Указываем кодировку UTF-8, разделитель ';', десятичный разделитель ','
file_path = 'cafe-sales.csv'
df = pd.read_csv(file_path, encoding='utf-8', sep=';', decimal=',')

# Шаг 2: общая выручка
# sum() вычисляет сумму всех значений в столбце 'Выручка,руб'
total_revenue = df['Выручка,руб'].sum()

# Шаг 3: средняя цена за чашку
# Добавляем новый столбец 'Цена за чашку' = Выручка / Чашек
# Затем вычисляем среднее значение этого столбца

df['Цена за чашку'] = df['Выручка,руб'] / df['Чашек']
average_price = df['Цена за чашку'].mean()

# Шаг 4: определяем лучшие и худшие дни по количеству проданных чашек
# idxmax() и idxmin() возвращают индекс строки с максимальным/минимальным значением
t_best = df.loc[df['Чашек'].idxmax(), 'День недели']
t_worst = df.loc[df['Чашек'].idxmin(), 'День недели']

# Шаг 5: вывод результатов
print(f"Общая выручка: {total_revenue:.2f} руб")
print(f"Средняя цена за чашку: {average_price:.2f} руб")
print(f"Лучший день по продажам чашек: {t_best}")
print(f"Худший день по продажам чашек: {t_worst}")

# Шаг 6: строим текстовый график продаж
print("\nТекстовый график продаж (каждый '*' = 10 чашек):")
for _, row in df.iterrows():
    stars = '*' * int(row['Чашек'] / 10)
    print(f"{row['День недели']}: {stars}")
